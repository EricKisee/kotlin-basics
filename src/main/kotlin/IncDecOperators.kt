fun main (){
    var number = 10
    println(number++)
    println(number)
    println(++number)

    var number2 = 0
    println(number2--)
    println(number2)
    println(--number2)

    //add
    println(10+10)
    //concatenate
    println("10"+"10")
    //conversion
    println(10.toString()+"10")

    println()
}