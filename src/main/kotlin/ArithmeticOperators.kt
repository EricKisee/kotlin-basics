import kotlin.math.PI
import kotlin.math.max
import kotlin.math.min
import kotlin.math.sqrt

fun main(){
    val number1 = 10
    val number2 = 3

    println("+ ${number1+number2}")
    println("- ${number1-number2}")
    println("/ ${number1/number2}")
    println("* ${number1*number2}")
    println("% ${number1%number2}")

    println(PI)
    println(max(10,20))
    println(min(20,30))
    println(sqrt(9.0))


}